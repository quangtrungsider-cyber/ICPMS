// Main Page
// ---------------------------------------------------------------------------

export function IcpmsRequirementsPage() {
  usePageTitle("Yêu cầu ICPMS");
  const organizationId = useOrganizationId();
  const environment = useRelayEnvironment();
  const { toast } = useToast();
  const [searchParams, setSearchParams] = useSearchParams();

  const selectedDocId = searchParams.get("docId") ?? "";
  const selectedVerId = searchParams.get("verId") ?? "";

  const [requirements, setRequirements] = useState<Requirement[]>([]);
  const [loading, setLoading] = useState(false);
  const [showGenerate, setShowGenerate] = useState(false);

  // Versions for step 2 (fetched when doc selected)
  const [versions, setVersions] = useState<VersionOpt[]>([]);
  const [versionsLoading, setVersionsLoading] = useState(false);

  const loadRequirements = useCallback(() => {
    if (!organizationId) return;
    setLoading(true);
    (fetchQuery(environment, ListQueryNode, { organizationId }, { networkCacheConfig: { force: true } }) as any)
      .toPromise()
      .then((data: any) => {
        setRequirements([...data.icpmsRequirementsForOrganization]);
      })
      .catch((err: Error) => {
        toast({ title: "Không thể tải yêu cầu", description: extractError(err), variant: "error" });
      })
      .finally(() => setLoading(false));
  }, [organizationId, environment, toast]);

  useEffect(() => { loadRequirements(); }, [loadRequirements]);

  // Fetch versions when a doc is selected (step 2)
  useEffect(() => {
    if (!selectedDocId) { setVersions([]); return; }
    setVersionsLoading(true);
    fetchQuery(environment, VersionsQueryNode, { documentId: selectedDocId }).subscribe({
      next: (data: unknown) => {
        const d = data as IcpmsDocumentVersionsTabQuery$data;
        if (d.document.__typename === "IcpmsDocument") {
          const edges = (d.document as any).versions?.edges ?? [];
          setVersions(edges.map((e: any) => e.node as VersionOpt));
        }
        setVersionsLoading(false);
      },
      error: () => setVersionsLoading(false),
    });
  }, [environment, selectedDocId]);

  // Derive document summaries from requirements
  const docSummaries = useMemo((): DocSummary[] => {
    const map = new Map<string, DocSummary>();
    for (const r of requirements) {
      if (!r.document) continue;
      const existing = map.get(r.document.id) ?? {
        id: r.document.id,
        code: (r.document as any).code ?? "",
        title: r.document.title,
        versionCount: 0,
        reqCount: 0,
        approvedCount: 0,
        needsReviewCount: 0,
        highPriorityCount: 0,
        _versionIds: new Set<string>(),
      } as DocSummary & { _versionIds: Set<string> };
      (existing as any)._versionIds.add(r.documentVersionId);
      existing.versionCount = (existing as any)._versionIds.size;
      existing.reqCount++;
      if (r.reviewStatus === "APPROVED") existing.approvedCount++;
      if (r.reviewStatus === "NEEDS_REVIEW") existing.needsReviewCount++;
      if (r.priority === "HIGH") existing.highPriorityCount++;
      map.set(r.document.id, existing);
    }
    return Array.from(map.values()).sort((a, b) => a.code.localeCompare(b.code));
  }, [requirements]);

  // Derive version summaries for selected doc
  const versionSummaries = useMemo((): VersionSummary[] => {
    const versionReqs = requirements.filter((r) => r.document?.id === selectedDocId);
    const map = new Map<string, VersionSummary>();
    for (const r of versionReqs) {
      const existing = map.get(r.documentVersionId) ?? {
        id: r.documentVersionId,
        versionCode: "",
        versionName: "",
        reqCount: 0,
        approvedCount: 0,
        needsReviewCount: 0,
        highPriorityCount: 0,
      };
      existing.reqCount++;
      if (r.reviewStatus === "APPROVED") existing.approvedCount++;
      if (r.reviewStatus === "NEEDS_REVIEW") existing.needsReviewCount++;
      if (r.priority === "HIGH") existing.highPriorityCount++;
      map.set(r.documentVersionId, existing);
    }
    // Fill in version code/name from fetched versions
    for (const v of versions) {
      const s = map.get(v.id);
      if (s) { s.versionCode = v.versionCode; s.versionName = v.versionName; }
    }
    return Array.from(map.values()).sort((a, b) => b.versionCode.localeCompare(a.versionCode));
  }, [requirements, selectedDocId, versions]);

  // Requirements for the selected version
  const versionRequirements = useMemo(() => {
    if (!selectedVerId) return [];
    return requirements.filter((r) => r.documentVersionId === selectedVerId);
  }, [requirements, selectedVerId]);

  // Delete mutations
  const [commitDeleteDoc, isDeletingDoc] = useMutation(DeleteDocMutationNode);
  const [commitDeleteVersion, isDeletingVersion] = useMutation(DeleteVersionMutationNode);
  const [confirmDelete, setConfirmDelete] = useState<{ type: "doc" | "version"; id: string; label: string } | null>(null);

  const handleDeleteDoc = useCallback((docId: string, label: string) => {
    setConfirmDelete({ type: "doc", id: docId, label });
  }, []);

  const handleDeleteVersion = useCallback((verId: string, label: string) => {
    setConfirmDelete({ type: "version", id: verId, label });
  }, []);

  const handleConfirmDelete = useCallback(() => {
    if (!confirmDelete) return;
    const { type, id } = confirmDelete;
    setConfirmDelete(null);
    if (type === "doc") {
      commitDeleteDoc({
        variables: { documentId: id },
        onCompleted: (data: unknown) => {
          const n = (data as any).deleteIcpmsRequirementsForDocument ?? 0;
          toast({ title: "Đã xóa", description: `Đã xóa ${n} yêu cầu của tài liệu`, variant: "success" });
          loadRequirements();
          setSearchParams({});
        },
        onError: (err) => toast({ title: "Lỗi", description: extractError(err), variant: "error" }),
      });
    } else {
      commitDeleteVersion({
        variables: { documentVersionId: id },
        onCompleted: (data: unknown) => {
          const n = (data as any).deleteIcpmsRequirementsForVersion ?? 0;
          toast({ title: "Đã xóa", description: `Đã xóa ${n} yêu cầu của phiên bản`, variant: "success" });
          loadRequirements();
          if (selectedVerId === id) setSearchParams({ docId: selectedDocId });
        },
        onError: (err) => toast({ title: "Lỗi", description: extractError(err), variant: "error" }),
      });
    }
  }, [confirmDelete, commitDeleteDoc, commitDeleteVersion, toast, loadRequirements, setSearchParams, selectedDocId, selectedVerId]);

  // Navigation helpers
  const selectDoc = (docId: string) => setSearchParams({ docId });
  const selectVer = (verId: string) => setSearchParams({ docId: selectedDocId, verId });
  const goBack = () => {
    if (selectedVerId) setSearchParams({ docId: selectedDocId });
    else setSearchParams({});
  };
  const goToRoot = () => setSearchParams({});

  const currentDoc = docSummaries.find((d) => d.id === selectedDocId);
  const currentVer = versions.find((v) => v.id === selectedVerId);

  const isDeleting = isDeletingDoc || isDeletingVersion;

  // Render based on navigation state
  if (selectedVerId) {
    return (
      <>
        <RequirementsListView
          doc={currentDoc}
          versionCode={currentVer?.versionCode ?? selectedVerId}
          versionName={currentVer?.versionName ?? ""}
          requirements={versionRequirements}
          loading={loading}
          onBack={goBack}
          onBackToDoc={goToRoot}
          onGenerate={() => setShowGenerate(true)}
          onDeleteVersion={() => handleDeleteVersion(selectedVerId, currentVer?.versionCode ?? selectedVerId)}
          loadRequirements={loadRequirements}
        />
        {showGenerate && (
          <GenerateDialog
            onClose={() => setShowGenerate(false)}
            onGenerated={loadRequirements}
            preselectedDocId={selectedDocId}
            preselectedVerId={selectedVerId}
          />
        )}
        {confirmDelete && (
          <ConfirmDeleteDialog
            label={confirmDelete.label}
            type={confirmDelete.type}
            onConfirm={handleConfirmDelete}
            onCancel={() => setConfirmDelete(null)}
            loading={isDeleting}
          />
        )}
      </>
    );
  }

  if (selectedDocId) {
    return (
      <>
        <VersionPickerView
          doc={currentDoc}
          versions={versionsLoading ? [] : versionSummaries}
          loading={versionsLoading}
          onSelect={selectVer}
          onBack={goBack}
          onGenerate={() => setShowGenerate(true)}
          onDeleteDoc={() => handleDeleteDoc(selectedDocId, currentDoc?.title ?? selectedDocId)}
          onDeleteVersion={handleDeleteVersion}
        />
        {showGenerate && (
          <GenerateDialog
            onClose={() => setShowGenerate(false)}
            onGenerated={loadRequirements}
            preselectedDocId={selectedDocId}
          />
        )}
        {confirmDelete && (
          <ConfirmDeleteDialog
            label={confirmDelete.label}
            type={confirmDelete.type}
            onConfirm={handleConfirmDelete}
            onCancel={() => setConfirmDelete(null)}
            loading={isDeleting}
          />
        )}
      </>
    );
  }

  return (
    <>
      <DocumentPickerView
        docs={docSummaries}
        loading={loading}
        onSelect={selectDoc}
        onRefresh={loadRequirements}
        onGenerate={() => setShowGenerate(true)}
        onDeleteDoc={handleDeleteDoc}
      />
      {showGenerate && (
        <GenerateDialog
          onClose={() => setShowGenerate(false)}
          onGenerated={loadRequirements}
        />
      )}
      {confirmDelete && (
        <ConfirmDeleteDialog
          label={confirmDelete.label}
          type={confirmDelete.type}
          onConfirm={handleConfirmDelete}
          onCancel={() => setConfirmDelete(null)}
          loading={isDeleting}
        />
      )}
    </>
  );
}