function RequirementsListView({
  doc,
  versionCode,
  versionName,
  requirements,
  loading,
  onBack,
  onBackToDoc,
  onGenerate,
  onDeleteVersion,
  loadRequirements,
}: {
  doc: DocSummary | undefined;
  versionCode: string;
  versionName: string;
  requirements: Requirement[];
  loading: boolean;
  onBack: () => void;
  onBackToDoc: () => void;
  onGenerate: () => void;
  onDeleteVersion: () => void;
  loadRequirements: () => void;
}) {
  const [selected, setSelected] = useState<Requirement | null>(null);
  const [localReqs, setLocalReqs] = useState<Requirement[]>(requirements);
  const [deletingOneId, setDeletingOneId] = useState<string | null>(null);
  const [filter, setFilter] = useState<FilterState>({
    search: "",
    requirementType: "",
    reviewStatus: "",
    applicabilityStatus: "",
    priority: "",
  });
  const { toast } = useToast();
  const [commitApprove, isApproving] = useMutation(ApproveMutationNode);
  const [commitDeleteOne] = useMutation(DeleteOneMutationNode);

  useEffect(() => { setLocalReqs(requirements); }, [requirements]);

  const handleSaved = useCallback((updated: Partial<Requirement>) => {
    setLocalReqs((prev) => prev.map((r) => (r.id === selected?.id ? { ...r, ...updated } : r)));
    setSelected((prev) => (prev ? { ...prev, ...updated } : prev));
  }, [selected]);

  // Derive the parseJobId from the first requirement (all reqs in a version share one parse job)
  const parseJobId = localReqs[0]?.parseJobId ?? null;

  const handleApproveAll = useCallback(() => {
    if (!parseJobId) return;
    commitApprove({
      variables: { parseJobId },
      onCompleted: (data: any) => {
        const count: number = data.approveIcpmsRequirementsForParseJob ?? 0;
        toast({ title: `Đã ký duyệt ${count} yêu cầu`, variant: "success" });
        loadRequirements();
      },
      onError: (err: Error) => {
        toast({ title: "Không thể ký duyệt", description: extractError(err), variant: "error" });
      },
    });
  }, [parseJobId, commitApprove, toast, loadRequirements]);

  const handleDeleteOne = useCallback((req: Requirement) => {
    if (!window.confirm(`Xoá yêu cầu "${req.requirementCode}"? Hành động này không thể hoàn tác.`)) return;
    setDeletingOneId(req.id);
    commitDeleteOne({
      variables: { id: req.id },
      onCompleted: () => {
        setDeletingOneId(null);
        toast({ title: "Đã xoá yêu cầu", description: req.requirementCode, variant: "success" });
        if (selected?.id === req.id) setSelected(null);
        setLocalReqs((prev) => prev.filter((r) => r.id !== req.id));
      },
      onError: (err: Error) => {
        setDeletingOneId(null);
        toast({ title: "Không thể xoá", description: extractError(err), variant: "error" });
      },
    });
  }, [commitDeleteOne, toast, selected]);

  const filtered = useMemo(() => {
    return localReqs.filter((r) => {
      if (filter.search) {
        const q = filter.search.toLowerCase();
        if (!r.title.toLowerCase().includes(q) && !r.requirementCode.toLowerCase().includes(q)) return false;
      }
      if (filter.requirementType && r.requirementType !== filter.requirementType) return false;
      if (filter.reviewStatus && r.reviewStatus !== filter.reviewStatus) return false;
      if (filter.applicabilityStatus && r.applicabilityStatus !== filter.applicabilityStatus) return false;
      if (filter.priority && r.priority !== filter.priority) return false;
      return true;
    }).sort((a, b) => a.requirementCode.localeCompare(b.requirementCode, undefined, { numeric: true, sensitivity: "base" }));
  }, [localReqs, filter]);

  const total = localReqs.length;
  const needsReview = localReqs.filter((r) => r.reviewStatus === "NEEDS_REVIEW").length;
  const approved = localReqs.filter((r) => r.reviewStatus === "APPROVED").length;
  const highPriority = localReqs.filter((r) => r.priority === "HIGH").length;
  const aiReviewed = localReqs.filter((r) => r.aiReviewedAt != null).length;
  const notApplicable = localReqs.filter((r) => r.applicabilityStatus === "NOT_APPLICABLE").length;
  const hasFilter = !!(filter.search || filter.requirementType || filter.reviewStatus || filter.applicabilityStatus || filter.priority);

  return (
    <div className="flex flex-col h-full">
      {/* Header + Breadcrumb */}
      <div className="px-6 py-4 border-b border-border-mid shrink-0">
        {/* Breadcrumb */}
        <div className="flex items-center gap-1 text-xs text-txt-tertiary mb-2">
          <button onClick={onBackToDoc} className="hover:text-txt-primary transition-colors">Yêu cầu ICPMS</button>
          <span>/</span>
          <button onClick={onBack} className="hover:text-txt-primary transition-colors max-w-[200px] truncate">{doc?.code} {doc?.title}</button>
          <span>/</span>
          <span className="text-txt-secondary font-medium">{versionCode}{versionName ? ` — ${versionName}` : ""}</span>
        </div>
        <div className="flex items-center justify-between">
          <div className="flex items-center gap-2">
            <button onClick={onBack} className="flex items-center gap-1 text-txt-tertiary hover:text-txt-primary transition-colors">
              <IconChevronLeft className="h-4 w-4" />
            </button>
            <h1 className="text-lg font-semibold text-txt-primary">{versionCode}</h1>
            {versionName && <span className="text-sm text-txt-tertiary">— {versionName}</span>}
          </div>
          <div className="flex items-center gap-2">
            <Button variant="tertiary" onClick={loadRequirements} disabled={loading}>
              <IconRotateCw className={`h-4 w-4 ${loading ? "animate-spin" : ""}`} />
            </Button>
            <Button variant="tertiary" onClick={onDeleteVersion}>
              <IconTrashCan className="h-4 w-4 mr-1.5" />
              Xóa phiên bản
            </Button>
            {parseJobId && localReqs.length > 0 && (
              <Button
                variant="secondary"
                onClick={handleApproveAll}
                disabled={isApproving}
                title="Ký duyệt tất cả yêu cầu đã được AI rà soát"
              >
                {isApproving ? "Đang ký duyệt..." : "✓ Ký duyệt tất cả"}
              </Button>
            )}
            <Button onClick={onGenerate}>
              <IconPlusLarge className="h-4 w-4 mr-1.5" />
              Tạo từ bản phân tích
            </Button>
          </div>
        </div>
      </div>

      {/* Stats bar */}
      {total > 0 && (
        <div className="flex items-center gap-5 px-6 py-2.5 border-b border-border-mid text-sm shrink-0">
          <div className="flex items-center gap-1.5">
            <span className="text-xl font-bold text-txt-primary">{total}</span>
            <span className="text-txt-tertiary text-xs">yêu cầu</span>
          </div>
          {aiReviewed > 0 && (
            <><div className="h-4 w-px bg-border-mid" /><div className="flex items-center gap-1.5"><Badge variant="info">{aiReviewed}</Badge><span className="text-xs text-txt-tertiary">AI đã rà soát</span></div></>
          )}
          {notApplicable > 0 && (
            <><div className="h-4 w-px bg-border-mid" /><div className="flex items-center gap-1.5"><Badge variant="neutral">{notApplicable}</Badge><span className="text-xs text-txt-tertiary">không áp dụng</span></div></>
          )}
          {needsReview > 0 && (
            <><div className="h-4 w-px bg-border-mid" /><div className="flex items-center gap-1.5"><Badge variant="warning">{needsReview}</Badge><span className="text-xs text-txt-tertiary">cần rà soát</span></div></>
          )}
          {approved > 0 && (
            <><div className="h-4 w-px bg-border-mid" /><div className="flex items-center gap-1.5"><Badge variant="success">{approved}</Badge><span className="text-xs text-txt-tertiary">đã duyệt</span></div></>
          )}
          {highPriority > 0 && (
            <><div className="h-4 w-px bg-border-mid" /><div className="flex items-center gap-1.5"><Badge variant="danger">{highPriority}</Badge><span className="text-xs text-txt-tertiary">ưu tiên cao</span></div></>
          )}
        </div>
      )}

      {/* Filter bar */}
      <div className="flex items-center gap-2.5 px-6 py-3 border-b border-border-mid flex-wrap shrink-0">
        <div className="flex-1 min-w-44 max-w-60">
          <Input variant="bordered" placeholder="Tìm theo tiêu đề, mã..." value={filter.search} onValueChange={(v) => setFilter((f) => ({ ...f, search: v }))} />
        </div>
        <Select value={filter.requirementType || undefined} onValueChange={(v) => setFilter((f) => ({ ...f, requirementType: v as IcpmsRequirementType }))} placeholder="Tất cả loại">
          {(Object.keys(REQ_TYPE_LABELS) as IcpmsRequirementType[]).map((t) => (<Option key={t} value={t}>{REQ_TYPE_LABELS[t]}</Option>))}
        </Select>
        <Select value={filter.reviewStatus || undefined} onValueChange={(v) => setFilter((f) => ({ ...f, reviewStatus: v as IcpmsRequirementReviewStatus }))} placeholder="Tất cả trạng thái">
          {(Object.keys(REVIEW_STATUS_LABELS) as IcpmsRequirementReviewStatus[]).map((s) => (<Option key={s} value={s}>{REVIEW_STATUS_LABELS[s]}</Option>))}
        </Select>
        <Select value={filter.applicabilityStatus || undefined} onValueChange={(v) => setFilter((f) => ({ ...f, applicabilityStatus: v as IcpmsApplicabilityStatus }))} placeholder="Tất cả mức áp dụng">
          {(Object.keys(APPLICABILITY_LABELS) as IcpmsApplicabilityStatus[]).map((s) => (<Option key={s} value={s}>{APPLICABILITY_LABELS[s]}</Option>))}
        </Select>
        <Select value={filter.priority || undefined} onValueChange={(v) => setFilter((f) => ({ ...f, priority: v as IcpmsRequirementPriority }))} placeholder="Tất cả ưu tiên">
          {(Object.keys(PRIORITY_LABELS) as IcpmsRequirementPriority[]).map((p) => (<Option key={p} value={p}>{PRIORITY_LABELS[p]}</Option>))}
        </Select>
        {hasFilter && (
          <Button variant="tertiary" onClick={() => setFilter({ search: "", requirementType: "", reviewStatus: "", applicabilityStatus: "", priority: "" })}>
            <IconCrossLargeX className="h-3 w-3 mr-1" />Xóa bộ lọc
          </Button>
        )}
        {hasFilter && filtered.length !== total && (
          <span className="text-xs text-txt-tertiary ml-auto">{filtered.length} / {total} yêu cầu</span>
        )}
      </div>

      {/* Table */}
      <div className="flex-1 overflow-auto px-6 py-4">
        {loading && localReqs.length === 0 ? (
          <div className="flex items-center justify-center py-16 text-txt-tertiary text-sm">Đang tải dữ liệu...</div>
        ) : filtered.length === 0 ? (
          <div className="flex flex-col items-center justify-center py-16 gap-2 text-txt-tertiary">
            <span className="text-sm">
              {localReqs.length === 0 ? "Phiên bản này chưa có yêu cầu nào." : "Không có yêu cầu nào khớp với bộ lọc."}
            </span>
            {localReqs.length === 0 && (
              <Button onClick={onGenerate}><IconPlusLarge className="h-4 w-4 mr-1.5" />Tạo từ bản phân tích</Button>
            )}
          </div>
        ) : (
          <Table>
            <Thead>
              <Tr>
                <Th width={48}>STT</Th>
                <Th width={140}>Mã</Th>
                <Th>Yêu cầu</Th>
                <Th width={110}>Nguồn</Th>
                <Th width={120}>Loại</Th>
                <Th width={130}>Trạng thái</Th>
                <Th width={130}>Mức áp dụng</Th>
                <Th width={90}>Ưu tiên</Th>
                <Th width={64}>Điểm</Th>
                <Th width={60}></Th>
              </Tr>
            </Thead>
            <Tbody>
              {filtered.map((req, idx) => (
                <Tr key={req.id} onClick={() => setSelected(req)} className={selected?.id === req.id ? "bg-blue-50" : ""}>
                  <Td noLink><span className="text-xs text-txt-tertiary tabular-nums">{idx + 1}</span></Td>
                  <Td noLink><span className="font-mono text-xs text-txt-tertiary">{req.requirementCode}</span></Td>
                  <Td noLink><span className="text-sm line-clamp-2 text-txt-primary">{req.title}</span></Td>
                  <Td noLink>
                    {req.sourceReference ? (
                      req.sourceSectionSummary ? (
                        <HoverTooltip
                          width={320}
                          trigger={
                            <span className="inline-block bg-blue-50 border border-blue-200 text-blue-700 text-[10px] font-medium px-1.5 py-0.5 rounded leading-tight cursor-default whitespace-nowrap">
                              {req.sourceReference}
                            </span>
                          }
                        >
                          <pre className="whitespace-pre-wrap font-sans leading-relaxed max-h-52 overflow-hidden" style={{ color: "#f9fafb" }}>
                            {req.sourceSectionSummary}
                          </pre>
                        </HoverTooltip>
                      ) : (
                        <span className="inline-block bg-blue-50 border border-blue-200 text-blue-700 text-[10px] font-medium px-1.5 py-0.5 rounded leading-tight cursor-default whitespace-nowrap">
                          {req.sourceReference}
                        </span>
                      )
                    ) : (
                      <span className="text-txt-tertiary text-[10px]">—</span>
                    )}
                  </Td>
                  <Td noLink><span className="text-xs text-txt-secondary">{REQ_TYPE_LABELS[req.requirementType]}</span></Td>
                  <Td noLink><Badge variant={REVIEW_STATUS_VARIANTS[req.reviewStatus]}>{REVIEW_STATUS_LABELS[req.reviewStatus]}</Badge></Td>
                  <Td noLink>
                    {req.applicabilityNote ? (
                      <HoverTooltip
                        width={288}
                        trigger={<Badge variant={APPLICABILITY_VARIANTS[req.applicabilityStatus]}>{APPLICABILITY_LABELS[req.applicabilityStatus]}</Badge>}
                      >
                        <p className="text-[10px] font-semibold uppercase tracking-wide mb-1" style={{ color: "#9ca3af" }}>Nhận xét AI</p>
                        <p className="leading-relaxed" style={{ color: "#f9fafb" }}>{req.applicabilityNote}</p>
                      </HoverTooltip>
                    ) : (
                      <Badge variant={APPLICABILITY_VARIANTS[req.applicabilityStatus]}>{APPLICABILITY_LABELS[req.applicabilityStatus]}</Badge>
                    )}
                  </Td>
                  <Td noLink><Badge variant={PRIORITY_VARIANTS[req.priority]}>{PRIORITY_LABELS[req.priority]}</Badge></Td>
                  <Td noLink><span className="text-xs font-mono text-txt-tertiary">{req.candidateScore}</span></Td>
                  <Td noLink>
                    <button
                      onClick={(e) => { e.stopPropagation(); handleDeleteOne(req); }}
                      disabled={deletingOneId === req.id}
                      className="p-1 rounded text-txt-tertiary hover:text-red-500 hover:bg-red-50 transition-all disabled:opacity-40"
                      title="Xoá yêu cầu này"
                    >
                      <IconTrashCan className="h-3.5 w-3.5" />
                    </button>
                  </Td>
                </Tr>
              ))}
            </Tbody>
          </Table>
        )}
      </div>

      {selected && <RequirementDetailPanel req={selected} onClose={() => setSelected(null)} onSaved={handleSaved} />}
    </div>
  );
}

