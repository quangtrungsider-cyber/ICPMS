function GenerateDialog({
  onClose,
  onGenerated,
  preselectedDocId,
  preselectedVerId,
}: {
  onClose: () => void;
  onGenerated: () => void;
  preselectedDocId?: string;
  preselectedVerId?: string;
}) {
  const env = useRelayEnvironment();
  const organizationId = useOrganizationId();
  const { toast } = useToast();

  const [docs, setDocs] = useState<DocOpt[]>([]);
  const [docsLoading, setDocsLoading] = useState(true);
  const [selectedDoc, setSelectedDoc] = useState<DocOpt | null>(null);

  const [versions, setVersions] = useState<VersionOpt[]>([]);
  const [versionsLoading, setVersionsLoading] = useState(false);
  const [selectedVersion, setSelectedVersion] = useState<VersionOpt | null>(null);

  const [parseJob, setParseJob] = useState<ParseJobOpt | null | undefined>(undefined);
  const [parseJobLoading, setParseJobLoading] = useState(false);
  const [isGenerating, setIsGenerating] = useState(false);
  const [progressPct, setProgressPct] = useState(0);
  const [error, setError] = useState("");

  const [commitGenerate] = useMutation(GenerateMutationNode);
  const [commitRunParser, isRunningParser] = useMutation(RunParserMutationNode);

  useEffect(() => {
    if (!isGenerating) { setProgressPct(0); return; }
    const interval = setInterval(() => {
      setProgressPct((prev) => prev >= 90 ? prev : prev + (90 - prev) * 0.06);
    }, 400);
    return () => clearInterval(interval);
  }, [isGenerating]);

  useEffect(() => {
    if (!organizationId) return;
    setDocsLoading(true);
    fetchQuery(env, DocsQueryNode, { organizationId }).subscribe({
      next: (data: unknown) => {
        const d = data as IcpmsDocumentsPageQuery$data;
        const edges = (d.organization as any).icpmsDocuments?.edges ?? [];
        const list: DocOpt[] = edges.map((e: any) => e.node as DocOpt);
        setDocs(list);
        if (preselectedDocId) {
          const pre = list.find((d) => d.id === preselectedDocId) ?? null;
          setSelectedDoc(pre);
        }
        setDocsLoading(false);
      },
      error: () => setDocsLoading(false),
    });
  }, [env, organizationId, preselectedDocId]);

  useEffect(() => {
    if (!selectedDoc) { setVersions([]); setSelectedVersion(null); return; }
    setVersionsLoading(true);
    fetchQuery(env, VersionsQueryNode, { documentId: selectedDoc.id }).subscribe({
      next: (data: unknown) => {
        const d = data as IcpmsDocumentVersionsTabQuery$data;
        if (d.document.__typename === "IcpmsDocument") {
          const edges = (d.document as any).versions?.edges ?? [];
          const list: VersionOpt[] = edges.map((e: any) => e.node as VersionOpt);
          setVersions(list);
          if (preselectedVerId) {
            const pre = list.find((v) => v.id === preselectedVerId) ?? null;
            setSelectedVersion(pre);
          }
        }
        setVersionsLoading(false);
      },
      error: () => setVersionsLoading(false),
    });
  }, [env, selectedDoc, preselectedVerId]);

  useEffect(() => {
    if (!selectedVersion) { setParseJob(undefined); return; }
    const ingJob = selectedVersion.latestIngestionJob;
    if (!ingJob || ingJob.status !== "COMPLETED") { setParseJob(null); return; }
    setParseJobLoading(true);
    fetchQuery(env, LatestParseJobQueryNode, { ingestionJobId: ingJob.id }).subscribe({
      next: (data: unknown) => {
        const d = data as IcpmsIngestionJobsPageLatestParseJobQuery$data;
        setParseJob(d.latestParseJobForIngestionJob ?? null);
        setParseJobLoading(false);
      },
      error: () => { setParseJob(null); setParseJobLoading(false); },
    });
  }, [env, selectedVersion]);

  const handleRunParser = useCallback(() => {
    const ingJobId = selectedVersion?.latestIngestionJob?.id;
    if (!ingJobId) return;
    commitRunParser({
      variables: { input: { ingestionJobId: ingJobId } },
      onCompleted: (data: unknown) => {
        const job = (data as any).createAndRunVietnameseParseJob?.parseJob ?? null;
        setParseJob(job as ParseJobOpt);
        if (!job) toast({ title: "Lỗi", description: "Parser chạy xong nhưng không trả về dữ liệu", variant: "error" });
      },
      onError: (err) => toast({ title: "Lỗi khi chạy parser", description: extractError(err), variant: "error" }),
    });
  }, [selectedVersion, commitRunParser, toast]);

  const handleGenerate = useCallback(() => {
    if (!parseJob || parseJob.status !== "COMPLETED") return;
    setIsGenerating(true);
    setError("");
    commitGenerate({
      variables: { input: { parseJobId: parseJob.id } },
      onCompleted: (data: unknown) => {
        const created = (data as any).generateRequirementsFromParseJob?.requirementsCreated ?? 0;
        setProgressPct(100);
        setTimeout(() => {
          toast({ title: "Thành công", description: `Đã tạo ${created} yêu cầu mới`, variant: "success" });
          onClose();
          setIsGenerating(false);
          setTimeout(onGenerated, 150);
        }, 400);
      },
      onError: (err) => { setError(extractError(err)); setIsGenerating(false); },
    });
  }, [parseJob, commitGenerate, toast, onGenerated, onClose]);

  const canGenerate = parseJob?.status === "COMPLETED" && !isGenerating;

  return (
    <div className="fixed inset-0 z-50 flex items-center justify-center bg-black/50 backdrop-blur-sm">
      <Card className="w-full max-w-lg">
        <div className="flex items-center justify-between px-6 py-4 border-b border-border-mid">
          <h2 className="text-base font-semibold text-txt-primary">Tạo yêu cầu từ bản phân tích</h2>
          <Button variant="tertiary" onClick={onClose}>
            <IconCrossLargeX className="h-4 w-4" />
          </Button>
        </div>

        <div className="px-6 py-5 space-y-4">
          <div className="space-y-1.5">
            <label className="text-xs font-medium text-txt-secondary">Tài liệu</label>
            <Select
              value={selectedDoc?.id || undefined}
              onValueChange={(v) => {
                const doc = docs.find((d) => d.id === v) ?? null;
                setSelectedDoc(doc);
                setSelectedVersion(null);
                setParseJob(undefined);
              }}
              placeholder={docsLoading ? "Đang tải..." : "Chọn tài liệu..."}
            >
              {docs.map((d) => (
                <Option key={d.id} value={d.id}>{d.code} — {d.title}</Option>
              ))}
            </Select>
          </div>

          {selectedDoc && (
            <div className="space-y-1.5">
              <label className="text-xs font-medium text-txt-secondary">Phiên bản</label>
              <Select
                value={selectedVersion?.id || undefined}
                onValueChange={(v) => {
                  const ver = versions.find((v2) => v2.id === v) ?? null;
                  setSelectedVersion(ver);
                  setParseJob(undefined);
                }}
                placeholder={versionsLoading ? "Đang tải..." : "Chọn phiên bản..."}
              >
                {versions.map((v) => (
                  <Option key={v.id} value={v.id}>{v.versionCode} — {v.versionName}</Option>
                ))}
              </Select>
            </div>
          )}

          {selectedVersion && (
            <div className="rounded-lg border border-border-mid p-3.5 text-sm bg-level-1">
              {parseJobLoading ? (
                <p className="text-txt-tertiary italic">Đang kiểm tra bản phân tích...</p>
              ) : selectedVersion.latestIngestionJob == null ? (
                <p className="text-txt-secondary">
                  Phiên bản này chưa có quá trình trích xuất. Vui lòng tạo trong mục <strong>Bóc tách tài liệu</strong> trước.
                </p>
              ) : selectedVersion.latestIngestionJob.status !== "COMPLETED" ? (
                <p className="text-txt-secondary">
                  Quá trình trích xuất chưa hoàn thành ({selectedVersion.latestIngestionJob.status}).
                </p>
              ) : parseJob == null ? (
                <div className="space-y-3">
                  <p className="text-txt-secondary">Tài liệu này chưa có bản phân tích cấu trúc.</p>
                  <Button variant="secondary" onClick={handleRunParser} disabled={isRunningParser}>
                    {isRunningParser ? "Đang chạy parser..." : "Chạy parser văn bản ngay"}
                  </Button>
                </div>
              ) : parseJob.status === "COMPLETED" ? (
                <div className="flex items-center gap-3">
                  <Badge variant="success">Sẵn sàng</Badge>
                  <span className="text-txt-secondary text-xs">
                    {parseJob.totalSections} mục
                  </span>
                </div>
              ) : (
                <p className="text-txt-secondary">Bản phân tích chưa hoàn thành ({parseJob.status}).</p>
              )}
            </div>
          )}

          {isGenerating && (
            <div className="space-y-2">
              <div className="flex items-center justify-between text-xs text-txt-secondary">
                <span>{progressPct < 100 ? `Đang tạo yêu cầu từ ${parseJob?.totalSections ?? 0} mục...` : "Hoàn thành!"}</span>
                <span className="font-mono font-medium">{Math.round(progressPct)}%</span>
              </div>
              <div className="w-full h-1.5 bg-border-mid rounded-full overflow-hidden">
                <div className="h-full bg-primary rounded-full transition-all duration-300" style={{ width: `${progressPct}%` }} />
              </div>
            </div>
          )}

          {error && <p className="text-xs text-danger-500 bg-danger-50 rounded px-3 py-2">{error}</p>}
        </div>

        <div className="flex gap-2 justify-end px-6 py-4 border-t border-border-mid">
          <Button variant="tertiary" onClick={onClose} disabled={isGenerating}>Hủy</Button>
          <Button onClick={handleGenerate} disabled={!canGenerate}>
            {isGenerating ? `Đang xử lý... ${Math.round(progressPct)}%` : "Tạo yêu cầu"}
          </Button>
        </div>
      </Card>
    </div>
  );
}

